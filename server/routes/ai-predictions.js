// GET /api/ai/health - Check AI engine + n8n health
router.get('/health', async (req, res) => {
  const health = { status: 'checking', ai_engine: null, n8n: null };

  // Check AI engine
  try {
    const aiRes = await axios.get(`${AI_ENGINE_URL}/health`, { timeout: 5000 });
    health.ai_engine = { status: 'connected', data: aiRes.data };
  } catch (err) {
    health.ai_engine = { status: 'disconnected', error: err.message };
  }

  // Check n8n
  try {
    const n8nRes = await axios.get(`${N8N_URL}/healthz`, { timeout: 5000 });
    health.n8n = {
      status: 'connected',
      url: N8N_URL,
      webhooksLoaded: Object.keys(n8nWebhooks).length,
      data: n8nRes.data
    };
  } catch (err) {
    health.n8n = { status: 'disconnected', error: err.message };
  }

  health.status = health.ai_engine?.status === 'connected' ? 'ok' : 'degraded';
  res.json(health);
});

