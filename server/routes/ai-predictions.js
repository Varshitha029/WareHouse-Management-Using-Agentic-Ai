const fs = require('fs');
const path = require('path');

// ==================== n8n + AI Engine Configuration ====================
const N8N_URL = process.env.N8N_URL || 'http://localhost:5678';
const AI_ENGINE_URL = process.env.AI_ENGINE_URL || 'http://localhost:8001';
const N8N_PROXY_TIMEOUT_MS = parseInt(process.env.N8N_PROXY_TIMEOUT_MS || '8000', 10);
const AI_ENGINE_TIMEOUT_MS = parseInt(process.env.AI_ENGINE_TIMEOUT_MS || '60000', 10);

function resolveAiTimeoutMs(aiEndpoint) {
  if (aiEndpoint === '/full-analysis') {
    return Math.max(AI_ENGINE_TIMEOUT_MS, 90000);
  }
  return AI_ENGINE_TIMEOUT_MS;
}

// Load n8n webhook config (generated by server/utils/n8nSetup.js)
let n8nWebhooks = {};
try {
  const configPath = path.join(__dirname, '..', 'config', 'n8nWebhooks.json');
  if (fs.existsSync(configPath)) {
    const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
    n8nWebhooks = config.webhooks || {};
    console.log(`✅ Loaded ${Object.keys(n8nWebhooks).length} n8n webhook routes`);
  } else {
    console.warn('⚠️  n8n webhook config not found. Run: node server/utils/n8nSetup.js');
  }
} catch (err) {
  console.warn('⚠️  Could not load n8n webhook config:', err.message);
}

// ==================== Smart Routing: n8n → AI Engine fallback ====================

/**
 * Routes request through n8n webhook first. Falls back to direct AI engine if n8n is down.
 * This ensures the app works with or without n8n running.
 *
 * @param {string} webhookKey - Key in n8nWebhooks config (e.g. 'inventory_analyze')
 * @param {string} aiEndpoint - Direct AI engine endpoint (e.g. '/inventory/analyze')
 * @param {string} method - HTTP method for direct fallback
 * @param {object} data - Request body
 * @param {object} query - Query parameters
 */
const proxyVian8n = async (webhookKey, aiEndpoint, method = 'POST', data = null, query = {}) => {
  const webhookUrl = n8nWebhooks[webhookKey];

  // Try n8n first (if webhook URL is configured)
  if (webhookUrl) {
    try {
      const response = await axios({
        method: 'POST',
        url: webhookUrl,
        data: { body: data || {}, query, originalEndpoint: aiEndpoint },
        timeout: N8N_PROXY_TIMEOUT_MS,
        headers: { 'Content-Type': 'application/json' }
      });
      console.log(`🔄 n8n: ${webhookKey} → OK`);
      return response.data;
    } catch (n8nError) {
      const status = n8nError?.response?.status;
      const statusText = n8nError?.response?.statusText;
      const message = n8nError?.message || statusText || (status ? `HTTP ${status}` : 'unknown error');
      console.warn(`⚠️  n8n unavailable for ${webhookKey} (${message}), falling back to direct AI`);
    }
  } else {
    console.warn(`⚠️  n8n webhook not configured for ${webhookKey}, using direct AI`);
  }

  // Fallback: call AI engine directly
  try {
    const config = {
      method,
      url: `${AI_ENGINE_URL}${aiEndpoint}`,
      params: query,
      timeout: resolveAiTimeoutMs(aiEndpoint)
    };
    if (data) config.data = data;
    const response = await axios(config);
    console.log(`⚡ Direct AI: ${aiEndpoint}`);
    return response.data;
  } catch (error) {
    console.error(`AI Engine error (${aiEndpoint}):`, error.message);
    throw new Error(error.response?.data?.detail || error.response?.data?.error || 'AI Engine unavailable');
  }
};

const normalizeLoanRiskRequest = (body = {}) => {
  const customerId = body.customerId || body.customer_id || body.user_id || null;
  const loanAmountRaw = body.loanAmount ?? body.loan_amount;
  const loanAmount = Number(loanAmountRaw);

  return {
    ...body,
    action: body.action || 'assess',
    customerId,
    customer_id: customerId,
    user_id: customerId,
    loanAmount: Number.isFinite(loanAmount) ? loanAmount : 0,
    loan_amount: Number.isFinite(loanAmount) ? loanAmount : 0
  };
};

const normalizeLoanRiskResponse = (raw = {}) => {
  const payload = raw?.results?.data || raw?.results || raw?.data || raw?.prediction || raw || {};

  const parsedScore = Number(payload?.risk_score ?? payload?.riskScore ?? payload?.score);
  const riskScore = Number.isFinite(parsedScore)
    ? Math.max(0, Math.min(100, parsedScore))
    : null;

  let recommendation = String(payload?.recommendation || payload?.decision || '').toLowerCase();
  if (!['approve', 'reject', 'conditional', 'review'].includes(recommendation)) {
    recommendation = '';
  }

  if (!recommendation && riskScore !== null) {
    recommendation = riskScore <= 35 ? 'approve' : riskScore <= 65 ? 'conditional' : 'reject';
  }

  return {
    ...payload,
    risk_score: riskScore,
    recommendation: recommendation || 'review',
    suggested_amount:
      payload?.suggested_amount ??
      payload?.suggestedAmount ??
      payload?.max_recommended_amount ??
      payload?.max_loan_recommended ??
      null,
    interest_adjustment: payload?.interest_adjustment ?? payload?.interestAdjustment ?? 0,
    reasons: payload?.reasons || payload?.risk_factors || payload?.factors || []
  };
};

