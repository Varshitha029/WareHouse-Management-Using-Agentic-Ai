from agents.base_agent import BaseAgent
from tools.gemini_client import GeminiClient
from tools.db_connector import DBConnector
from config.prompts import CHAT_AGENT_PROMPT
import json


class EmailAgent(BaseAgent):
    """
    AI Email Agent — generates personalised email content for customer outreach.
    Handles loan reminders, payment alerts, storage expiry notices, and
    general warehouse communications. All content is generated by the LLM
    using real customer/loan data from the database.
    """

    def __init__(self):
        super().__init__("EmailAgent", "AI-powered email content generation for customer outreach")

    async def process(self, data: dict) -> dict:
        try:
            action     = data.get('action', 'generate')
            role       = data.get('role', 'owner')
            message    = data.get('message', '')

            if action == 'generate':
                return await self._generate_email(data)
            elif action == 'loan_reminder':
                return await self._generate_loan_reminder(data)
            elif action == 'storage_notice':
                return await self._generate_storage_notice(data)
            elif action == 'payment_alert':
                return await self._generate_payment_alert(data)
            elif action == 'bulk_outreach':
                return await self._generate_bulk_outreach(data)
            else:
                return await self._generate_email(data)

        except Exception as e:
            return {
                "success": False,
                "error": str(e),
                "message": "Email Agent encountered an error"
            }

    # ------------------------------------------------------------------ #
    #  Internal helpers                                                    #
    # ------------------------------------------------------------------ #

    async def _get_db_context(self, role: str, user_id: str = None) -> dict:
        """Fetch relevant DB data for richer email content."""
        try:
            db = DBConnector()
            ctx = {}
            loans    = await db.get_loans()
            if loans:
                overdue = [l for l in loans if l.get('status') in ('overdue', 'defaulted')]
                pending = [l for l in loans if l.get('status') == 'approved']
                ctx['overdue_loans']  = len(overdue)
                ctx['pending_loans']  = len(pending)
                ctx['total_loans']    = len(loans)
                ctx['overdue_sample'] = overdue[:3]
            inventory = await db.get_inventory()
            if inventory:
                ctx['total_stock_bags'] = sum(i.get('totalBags', 0) for i in inventory)
            return ctx
        except Exception:
            return {}

    async def _generate_email(self, data: dict) -> dict:
        """Generic email generation from a prompt."""
        purpose    = data.get('purpose', 'general communication')
        customer   = data.get('customerName', 'Valued Customer')
        extra_ctx  = data.get('extraContext', '')

        prompt = f"""You are an AI assistant helping a warehouse manager draft a professional email.

Purpose: {purpose}
Customer Name: {customer}
Additional Context: {extra_ctx}

Write a professional, friendly email. Include:
- A clear subject line (prefix with "Subject: ")
- A proper greeting
- A concise body with the key message
- A polite closing

Use formal but warm language suitable for a grain warehouse business."""

        raw = await GeminiClient.generate_text(prompt, CHAT_AGENT_PROMPT)

        # Split subject from body if the LLM included it
        subject, body = 'Message from Warehouse Management System', raw
        for line in raw.split('\n'):
            if line.lower().startswith('subject:'):
                subject = line.split(':', 1)[1].strip()
                body    = raw[raw.find('\n') + 1:].strip()
                break

        return {
            "success":   True,
            "agent":     "email",
            "action":    "generate",
            "data": {
                "subject": subject,
                "body":    body,
                "reply":   f"Email generated:\n\n**Subject:** {subject}\n\n{body}",
            }
        }

    async def _generate_loan_reminder(self, data: dict) -> dict:
        """Generate a personalised loan repayment reminder."""
        customer_name  = data.get('customerName', 'Customer')
        loan_amount    = data.get('loanAmount', 0)
        due_date       = data.get('dueDate', 'as soon as possible')
        days_overdue   = data.get('daysOverdue', 0)
        grain_type     = data.get('grainType', '')
        loan_id        = data.get('loanId', '')

        urgency = "urgent" if days_overdue > 30 else "friendly"

        prompt = f"""You are an AI assistant for a grain warehouse. Write a {urgency} loan repayment reminder email.

Customer: {customer_name}
Outstanding Loan Amount: ₹{loan_amount:,.0f}
Due Date: {due_date}
Days Overdue: {days_overdue} days
Grain Collateral: {grain_type}
Loan Reference: {loan_id}

Write a professional email reminder. Include:
- Subject line (prefix "Subject: ")
- Polite but firm reminder of the outstanding amount
- Clear due date and payment instructions
- Mention of the grain collateral if applicable
- Contact details for queries
- Use ₹ for currency and Indian numbering (lakhs/crores)

{'Use a firm tone emphasising urgency and possible consequences.' if days_overdue > 30 else 'Use a polite, friendly tone.'}"""

        raw = await GeminiClient.generate_text(prompt, CHAT_AGENT_PROMPT)

        subject = f"Loan Repayment Reminder — ₹{loan_amount:,.0f} Due"
        body    = raw
        for line in raw.split('\n'):
            if line.lower().startswith('subject:'):
                subject = line.split(':', 1)[1].strip()
                body    = '\n'.join(raw.split('\n')[1:]).strip()
                break

        return {
            "success": True,
            "agent":   "email",
            "action":  "loan_reminder",
            "data": {
                "subject":     subject,
                "body":        body,
                "reply":       f"Loan reminder email generated for {customer_name}:\n\n**Subject:** {subject}\n\n{body}",
                "customerName": customer_name,
                "loanAmount":   loan_amount,
            }
        }

